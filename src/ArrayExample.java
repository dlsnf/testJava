

public class ArrayExample {
    public static void main(String[] args) {
        // 배열 초기화
        int[] array = {5, 1, 3};

        // 배열 출력
        System.out.println("입력된 배열 값:");
        for (int i = 0; i < array.length; i++) {
            System.out.print(array[i]);
        }
        System.out.println("");
    }
}
